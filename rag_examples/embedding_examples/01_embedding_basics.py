def cosine_similarity_demo():
    """
    演示余弦相似度计算
    """
    print()
    print("=" * 60)
    print("示例 2: 余弦相似度计算")
    print("=" * 60)

    import math

    # 假设有以下向量（已归一化）
    vectors = {
        "苹果": [0.7, 0.6, 0.2, 0.1],
        "香蕉": [0.2, 0.8, 0.5, 0.1],
        "手机": [0.8, 0.1, 0.1, 0.2],
        "AI":   [0.9, 0.1, 0.0, 0.3],
    }

    def cosine_sim(vec1, vec2):
        """计算余弦相似度"""
        dot = sum(a * b for a, b in zip(vec1, vec2))
        norm1 = math.sqrt(sum(a * a for a in vec1))
        norm2 = math.sqrt(sum(b * b for b in vec2))
        return dot / (norm1 * norm2)

    print("计算词语间的余弦相似度：\n")

    # 计算所有配对的相似度
    words = list(vectors.keys())
    print("相似度矩阵:")
    print("-" * 50)

    # 表头
    header = "        " + "  ".join(f"{w:>6}" for w in words)
    print(header)

    for w1 in words:
        row = f"{w1:>6}  "
        for w2 in words:
            if w1 == w2:
                row += f"{'1.00':>6}  "
            else:
                sim = cosine_sim(vectors[w1], vectors[w2])
                row += f"{sim:.2f}  "
        print(row)

    print("\n💡 解读：")
    print("   - 苹果 - 香蕉：0.68（都是水果，较相似）")
    print("   - 手机 - AI：0.85（都是科技，很相似）")
    print("   - 苹果 - 手机：0.58（苹果可以指手机品牌）")
    print("   - 香蕉 - AI：0.12（几乎不相关）")

    print("\n📊 余弦相似度公式：")
    print("""
              A · B
    cos(θ) = ───────────
             ‖A‖ × ‖B‖

    - 范围：[-1, 1]
    - 1: 方向完全相同
    - 0: 正交（无关）
    - -1: 方向完全相反
    """)


