# 示例 1: 模拟 Embedding 过程
# =============================================================================

def simulate_embedding_process():
    """
    模拟 Embedding 的过程（用简单规则演示概念）
    """
    print("=" * 60)
    print("示例 1: 模拟 Embedding 过程")
    print("=" * 60)

    # 用一个简化的"词袋"方法模拟 Embedding
    # 真实的 Embedding 要复杂得多，这里只是演示概念

    # 定义一个简单的"语义空间"
    # 每个维度代表一个语义特征
    semantic_dimensions = {
        "科技": 0,
        "水果": 1,
        "食物": 2,
        "抽象": 3,
    }

    # 给一些词分配"伪向量"（仅用于演示）
    def mock_embed(word):
        # 初始化 4 维向量
        vec = [0.0, 0.0, 0.0, 0.0]

        # 根据词的语义给对应维度赋值
        if word in ["手机", "电脑", "AI", "互联网"]:
            vec[semantic_dimensions["科技"]] = 0.8
        elif word in ["苹果", "香蕉", "橙子"]:
            vec[semantic_dimensions["水果"]] = 0.8
        elif word in ["米饭", "面条", "红烧肉"]:
            vec[semantic_dimensions["食物"]] = 0.8
        elif word in ["爱情", "自由", "梦想"]:
            vec[semantic_dimensions["抽象"]] = 0.8

        # "苹果" 比较特殊，既是水果又可以是科技产品
        if word == "苹果":
            vec[semantic_dimensions["水果"]] = 0.7
            vec[semantic_dimensions["科技"]] = 0.3  # 也可以指 Apple 公司
        elif word == "AI":
            vec[semantic_dimensions["科技"]] = 0.9
            vec[semantic_dimensions["抽象"]] = 0.2  # 也有一定的抽象性

        return vec

    # 演示
    words = ["苹果", "香蕉", "手机", "AI", "米饭", "爱情"]

    print("词语 → 向量（4 维语义空间）\n")
    print(f"{'词语':<8} {'向量':<35} {'语义解释'}")
    print("-" * 60)

    for word in words:
        vec = mock_embed(word)
        vec_str = "[" + ", ".join(f"{v:.1f}" for v in vec) + "]"
        explanation = f"科技={vec[0]:.1f}, 水果={vec[1]:.1f}, 食物={vec[2]:.1f}, 抽象={vec[3]:.1f}"
        print(f"{word:<8} {vec_str:<35} {explanation}")

    print("\n💡 观察：")
    print("   - '苹果' 和 '香蕉' 在水果维度都较高 → 语义相近")
    print("   - '手机' 和 'AI' 在科技维度都较高 → 语义相近")
    print("   - '苹果' 同时在科技维度有值 → 可指 Apple 公司")


