def visualize_embeddings():
    """
    用简化的方式演示向量可视化

    真实向量是高维的（768 维、1024 维），
    这里用 2D 演示帮助理解
    """
    print()
    print("=" * 60)
    print("示例 4: 向量可视化（2D 简化）")
    print("=" * 60)

    # 模拟一些词语的 2D 向量
    words_2d = {
        "苹果": (0.7, 0.5),
        "香蕉": (0.5, 0.7),
        "橙子": (0.6, 0.6),
        "手机": (-0.5, 0.8),
        "电脑": (-0.7, 0.7),
        "AI": (-0.6, 0.9),
        "米饭": (0.8, -0.3),
        "面条": (0.7, -0.4),
        "汽车": (-0.8, 0.2),
        "飞机": (-0.9, 0.3),
    }

    # 用 ASCII 艺术简单可视化
    print("语义空间可视化（2D 平面）：\n")

    # 定义画布
    size = 21
    canvas = [[" " for _ in range(size)] for _ in range(size)]

    # 映射坐标到画布
    for word, (x, y) in words_2d.items():
        # 从 [-1, 1] 映射到 [0, 20]
        cx = int((x + 1) * (size - 1) / 2)
        cy = int((1 - y) * (size - 1) / 2)  # y 轴翻转

        if 0 <= cx < size and 0 <= cy < size:
            # 放置词语的首字
            canvas[cy][cx] = word[0]

    # 打印画布
    print("     科技/数码")
    print("     ↑")
    print("  手机  电脑  AI")
    print("     |")
    print("  苹果  橙子")
    print("     |     香蕉")
    print("     |         食物")
    print("     |    米饭  面条")
    print("     |")
    print("  汽车     飞机")
    print("     └─────────────────→")

    print("\n💡 观察：")
    print("   - 左上：科技/数码（手机、电脑、AI）")
    print("   - 中上：水果（苹果、香蕉、橙子）")
    print("   - 右下：食物（米饭、面条）")
    print("   - 左下：交通工具（汽车、飞机）")
    print()
    print("   语义相近的词在空间中距离更近！")


