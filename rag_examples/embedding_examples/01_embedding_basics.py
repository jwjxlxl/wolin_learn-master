"""
什么是 Embedding？

📊 定义
   Embedding = 嵌入 = 将离散对象映射到连续向量空间
   在 NLP 中，就是将文本转换为数字向量

💡 生活化比喻
   Embedding = "给文字分配 GPS 坐标"

   传统文本："苹果" 就是一串字符
   Embedding 后："苹果" → [0.8, -0.2, 0.5, ...] → 向量空间中的一个点

   语义相近的词，在向量空间中的距离也相近：

                    ↑
                    |  "橙子"
               "苹果" ●
                    |      ● "香蕉"
                    |
         "手机" ●   |           ● "水果"
                    |
   ─────────────────┼──────────────────→
                    |

📦 为什么需要 Embedding？

   计算机无法直接理解文字，但擅长处理数字：

   文字 → Embedding → 向量 → 计算机可以：
                        - 计算相似度
                        - 聚类分析
                        - 机器学习训练

🔑 核心特性

   1. 语义保持
      语义相近的文本，向量距离也近

   2. 固定维度
      无论文本多长，输出向量长度固定

   3. 可计算
      支持向量加减、相似度计算
"""


