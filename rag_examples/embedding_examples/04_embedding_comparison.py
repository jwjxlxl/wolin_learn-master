def similarity_comparison_simulated():
    """
    模拟不同模型的相似度计算效果
    """
    print()
    print("=" * 60)
    print("示例 2: 相似度计算对比（模拟）")
    print("=" * 60)

    # 模拟不同模型对同一文本对的相似度评分
    # 实际数据会有所不同，这里仅用于演示概念

    text_pairs = [
        ("机器学习", "深度学习", "语义高度相关"),
        ("机器学习", "人工智能", "语义相关"),
        ("机器学习", "红烧肉", "语义无关"),
        ("自然语言处理", "文本分析", "语义相关"),
        ("计算机视觉", "图像处理", "语义高度相关"),
        ("Python 编程", "红烧肉做法", "语义无关"),
    ]

    # 模拟各模型的评分（实际会有差异）
    model_scores = {
        "bge-large-zh": [0.88, 0.75, 0.12, 0.82, 0.85, 0.08],
        "bge-base-zh":  [0.86, 0.73, 0.15, 0.80, 0.83, 0.10],
        "m3e-base":     [0.85, 0.72, 0.18, 0.78, 0.81, 0.12],
        "text2vec":     [0.82, 0.68, 0.22, 0.75, 0.78, 0.18],
    }

    print("不同模型对文本对相似度的评分对比：\n")
    print("-" * 60)

    for i, (text1, text2, relation) in enumerate(text_pairs):
        print(f"\n文本对 {i+1}: '{text1}' ↔ '{text2}'")
        print(f"  语义关系：{relation}")
        print()

        for model_name, scores in model_scores.items():
            score = scores[i]
            bar_len = int(score * 20)
            bar = "█" * bar_len + "░" * (20 - bar_len)
            print(f"  {model_name}: {bar} {score:.2f}")

    print("\n💡 观察：")
    print("   - 大模型（bge-large）通常区分度更好")
    print("   - 语义无关的文本对，得分应接近 0")
    print("   - 语义相关的文本对，得分应在 0.7 以上")


